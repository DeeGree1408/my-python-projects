# Моя первая программа на Python (с ностальгией по БК)

print("=== ПРОСТОЙ КАЛЬКУЛЯТОР ===")

# Просим числа у пользователя
a = input("Введи первое число: ")
b = input("Введи второе число: ")

# input() возвращает текст, превращаем его в целые числа
a = int(a)
b = int(b)

# Спрашиваем, что делать
print("Что делаем?")
print("1 - Сложить")
print("2 - Вычесть")
print("3 - Умножить")
print("4 - Разделить")

choice = input("Твой выбор (1-4): ")

# Проверяем выбор и печатаем результат
if choice == "1":
    print("Результат:", a + b)
elif choice == "2":
    print("Результат:", a - b)
elif choice == "3":
    print("Результат:", a * b)
elif choice == "4":
    # Проверяем, не ноль ли второе число
    if b == 0:
        print("Ошибка! На ноль делить нельзя.")
    else:
        print("Результат:", a / b)
else:
    print("Неправильный выбор, нужно ввести от 1 до 4")

print("=== КОНЕЦ ===")

